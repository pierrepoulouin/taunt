package com.taunt.model;

public class Licence {
	private int idLicence;
	private String nomLicence;
	private String typeLicence;
	
	public int getIdLicence() {
		return idLicence;
	}
	public void setIdLicence(int idLicence) {
		this.idLicence = idLicence;
	}
	public String getNomLicence() {
		return nomLicence;
	}
	public void setNomLicence(String nomLicence) {
		this.nomLicence = nomLicence;
	}
	public String getTypeLicence() {
		return typeLicence;
	}
	public void setTypeLicence(String typeLicence) {
		this.typeLicence = typeLicence;
	}


}
